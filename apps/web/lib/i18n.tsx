type I18nCtx = {
  lang: Lang;
  dir: "ltr" | "rtl";
  t: (key: string) => string;
  setLang: (l: Lang) => void;
  toggleLang: () => void;
};

const Ctx = createContext<I18nCtx | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  // Restore saved language on mount.
  useEffect(() => {
    const saved = (localStorage.getItem("lang") as Lang) || "en";
    setLangState(saved);
  }, []);

  // Keep <html lang/dir> in sync so RTL + fonts apply globally.
  useEffect(() => {
    const dir = lang === "ar" ? "rtl" : "ltr";
    document.documentElement.lang = lang;
    document.documentElement.dir = dir;
    localStorage.setItem("lang", lang);
  }, [lang]);

  const dir = lang === "ar" ? "rtl" : "ltr";
  const t = (key: string) => DICT[lang][key] ?? key;
  const setLang = (l: Lang) => setLangState(l);
  const toggleLang = () => setLangState((p) => (p === "en" ? "ar" : "en"));

  return <Ctx.Provider value={{ lang, dir, t, setLang, toggleLang }}>{children}</Ctx.Provider>;
}

export function useI18n() {
  const ctx = useContext(Ctx);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}
